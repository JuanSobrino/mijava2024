class Ejemplo2 {

    public static void main (String[] args) {

        //Declaración de variable llamada numero1, de tipo entero
        int numero1;

        //Declaración de variable llamada numero2, de tipo entero
        int numero2;

        //Declaración de variable llamada resultado, de tipo entero
        int resultado;

        //Asignar  el valor 39 a la variable numero 1
        numero1 = 39;

        //Asignar  el valor 12 a la variable numero 2
        numero2 = 12;

        //Suma numero 1 más numero2, y el resultado lo graba en la variable resultado
        resultado = numero1 + numero2;

        //Imprime por consola el valor de la variable `resultado´
        System.out.println(resultado); 
    }
}
